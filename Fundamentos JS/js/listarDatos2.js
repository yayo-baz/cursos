// Destructuring de arrays

const array = ['HTML', 'CSS', 'JS', 'React'];

const [var1, var2] = array; // Se crean variables que obtienen el valor segun la posicion del array original

const array2 = ['HTML', 'CSS', 'JS', 'React'];

const [, , , var4] = array2; // Usando comas en destructuring podemos obviar la creacion de variables por cada espacio del array
